    db = SessionLocal()
    try:
        existing = db.query(Medicine).count()
        if existing > 0:
            print(f"Seed sudah ada ({existing} produk). Skip.")
            return
        
        for data in MEDICINES:
            med = Medicine(**data)
            db.add(med)
        
        db.commit()
        print(f"✓ Seed selesai: {len(MEDICINES)} produk berhasil ditambahkan.")
    except Exception as e:
        db.rollback()
        print(f"✗ Seed gagal: {e}")
        raise
    finally:
        db.close()

